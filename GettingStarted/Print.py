x, y, z = 3, 4, 5

# print all variables with one print statement
print(x, y, z)  # output is 3 4 5 --> horizontaly written
print(x + y + z)  # add all values together

# if done with strings it will print all 3 words together --> HelloWorld!
a, b, c = "Hello", "World", "!"
print(a + b + c)

# to print out strings and ints together use comma --> 3 Hello (Will add a space between 3 and Hello automatically)
q = 3
w = "Hello"

print(q, w)
